        private static Type GetSimpleType(Type type)
        {
            if (type.IsValueType)
            {
                if (type == typeof(UInt64)) return typeof(string);
                if (type.ToString().ToLower().Contains("int"))
                    return typeof(Int64);
                return type == typeof(bool) ? type : typeof(string);
            }
            return typeof(string);
        }
