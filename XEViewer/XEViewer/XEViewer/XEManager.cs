    public class Sql
    {
        public static void SaveToDb(string server, string database, DataTable dt)
        {
            var conn = new SqlConnection(GetConnectionString(server, database));
            conn.Open();
            using (var cmd = new SqlCommand(GetCreateTable(dt), conn))
            {
                cmd.ExecuteNonQuery();
            }
            using (var bc = new SqlBulkCopy(conn))
            {
                bc.BulkCopyTimeout = 300;
                bc.DestinationTableName = dt.TableName;
                bc.WriteToServer(dt);
            }
            conn.Close();
        }

        public static string GetConnectionString(string server, string database)
        {
            var cb = new SqlConnectionStringBuilder
            {
                ConnectTimeout = 60,
                IntegratedSecurity = true,
                DataSource = server,
                InitialCatalog = database,
                ApplicationName = "XelParser"
            };
            return cb.ConnectionString;
        }

        public static string GetCreateTable(DataTable dataTable)
        {
            var sb = new StringBuilder();
            DataTypes dt = new DataTypes();
            sb.AppendFormat("CREATE TABLE {0} (", dataTable.TableName);
            var lookup = new DataTypes();
            foreach (DataColumn column in dataTable.Columns)
                sb.AppendFormat("{0} {1},", column.ColumnName, GetSqlType(dt, column));
            sb.AppendLine(")");
            return sb.ToString();
        }

        public static object GetSqlType(DataTypes dt, DataColumn column)
        {
            if (dt.SqlDataTypes.ContainsKey(column.ColumnName))
                return dt.SqlDataTypes[column.ColumnName];
            else
            {
                var dataType = column.DataType;
                if (dataType == typeof(Int64)) return "bigint";
                if (dataType == typeof(bool)) return "bit";
            }
            return "varchar(4000)";
        }
    }
